package entity;

import java.util.Date;

/**
 *
 * @author Ng·ªçc Lan
 */
public class Player {

    private String name;
    private Date date;
    private int score;

    public Player() {
    }

    public Player(String name, Date date, int score) {
        this.name = name;
        this.date = date;
        this.score = score;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

}
